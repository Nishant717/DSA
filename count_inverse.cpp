class Solution{
  public:
    // arr[]: Input Array
    // N : Size of the Array arr[]
    // Function to count inversions in the array.
    long long int inversionCount(long long arr[], long long N)
    {
        // Your Code Here
        long long int count=0;
        long long int i,j;
        j=1;
        for(i=0;i<N;i++)
        {
            if(arr[i]>arr[j])
            {
                count++;
            }
            j+=1;
        }
        return count;
    }
    

};

